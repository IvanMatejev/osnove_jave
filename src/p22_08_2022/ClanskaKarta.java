package p22_08_2022;

public class ClanskaKarta {
	private int popust;
	private String broj;
	public ClanskaKarta(int popust, String broj) {
		super();
		this.popust = popust;
		this.broj = broj;
	}
	public int getPopust() {
		return popust;
	}
	public void setPopust(int popust) {
		this.popust = popust;
	}
	public String getBroj() {
		return broj;
	}
	public void setBroj(String broj) {
		this.broj = broj;
	}
	
	
}
